class AuditGateError(RuntimeError):
    """A worker dependency audit contract was not satisfied."""


@dataclass(frozen=True)
class ApprovedSuppression:
    package: str
    version: str
    evidence: frozenset[str]
    owner: str
    review_by: date
    expires_on: date


@dataclass(frozen=True)
class Suppression:
    advisory_id: str
    package: str
    version: str
    justification: str
    evidence: tuple[str, ...]
    owner: str
    review_by: date
    expires_on: date


@dataclass(frozen=True)
class Finding:
    advisory_id: str
    package: str
    version: str
    aliases: tuple[str, ...]
    fix_versions: tuple[str, ...]


